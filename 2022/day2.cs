using System.Linq;
using System;
					
public class Program
{
	public static void Main()
	{
		var tournament = input.Split('\n').Select(x => x.Trim(' ', '\n').Split(' '));
		var scoreFirst = tournament.Aggregate(0, (val, next) => val + Shape(next[1]) + Score(next[0], next[1]));
		var scoreSecond = tournament.Aggregate(0, (val, next) => { var chosen = ChooseShape(next); return val + Shape(chosen) + Score(next[0], chosen); });
		Console.WriteLine(scoreFirst + " " + scoreSecond);
	}
	
	public static int Shape(string s) => s.Trim() switch 
	{
		"X" or "A" => 1, // Rock
		"Y" or "B" => 2, // Paper
		"Z" or "C" => 3, // Scissors
		_ => 0
	};

	public static int Score(string _op, string _me)
	{
		int op = Shape(_op);
		int me = Shape(_me);
		if (op - me == -1 || op - me  == 2) return 6;
		if (op == me) return 3;
		return 0;
	}
	
	public static string ChooseShape(string[] ss)
	{
		int op = Shape(ss[0]);
		int me = Shape(ss[1]);
		return op switch
		{
		    1 when me == 1 => "Z",
			1 when me == 2 => "X",
			1 when me == 3 => "Y",
			2 when me == 1 => "X",
			2 when me == 2 => "Y",
			2 when me == 3 => "Z",
			3 when me == 1 => "Y",
			3 when me == 2 => "Z",
			3 when me == 3 => "X",
			_ => ""
		};
	}
	
